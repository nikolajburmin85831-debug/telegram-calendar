package io.github.nadya.assistant.adapter.out.persistence.jdbc;

import javax.sql.DataSource;
import java.sql.Connection;
import java.sql.SQLException;
import java.sql.Statement;

public final class JdbcPersistenceSchemaInitializer {

    private final JdbcPersistenceSupport support;

    public JdbcPersistenceSchemaInitializer(DataSource dataSource, JdbcPersistenceSettings settings) {
        this.support = new JdbcPersistenceSupport(dataSource, settings.schema());
    }

    public void initialize() {
        try (Connection connection = support.openConnection(); Statement statement = connection.createStatement()) {
            statement.execute("CREATE SCHEMA IF NOT EXISTS " + support.schema());
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS %s (
                        conversation_id VARCHAR(255) PRIMARY KEY,
                        payload TEXT NOT NULL,
                        updated_at_ms BIGINT NOT NULL
                    )
                    """.formatted(support.table("conversation_state")));
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS %s (
                        user_id VARCHAR(255) PRIMARY KEY,
                        payload TEXT NOT NULL,
                        updated_at_ms BIGINT NOT NULL
                    )
                    """.formatted(support.table("user_context")));
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS %s (
                        idempotency_key VARCHAR(512) PRIMARY KEY,
                        created_at_ms BIGINT NOT NULL
                    )
                    """.formatted(support.table("idempotency_marker")));
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS %s (
                        audit_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        user_id VARCHAR(255) NOT NULL,
                        conversation_id VARCHAR(255) NOT NULL,
                        event_type VARCHAR(255) NOT NULL,
                        occurred_at_ms BIGINT NOT NULL,
                        details TEXT NOT NULL
                    )
                    """.formatted(support.table("audit_entry")));
            statement.execute("""
                    CREATE INDEX IF NOT EXISTS idx_%s_idempotency_created_at
                    ON %s (created_at_ms)
                    """.formatted(support.schema(), support.table("idempotency_marker")));
            statement.execute("""
                    CREATE INDEX IF NOT EXISTS idx_%s_audit_occurred_at
                    ON %s (occurred_at_ms)
                    """.formatted(support.schema(), support.table("audit_entry")));
        } catch (SQLException exception) {
            throw support.operationFailed("Schema initialization", exception);
        }
    }
}
